import Foundation


class Artist{
    
    var naam:String
    var omschrijving:String
    var uur:String
    var img:String
    var music:String
    
    init(naam:String, omschrijving:String, uur:String, img:String, music:String) {
        self.naam = naam
        self.omschrijving = omschrijving
        self.uur = uur
        self.img = img
        self.music = music
        
    }
    
}

